    public pinnedSibling?: RowNode<TData>;

    /** @inheritDoc */
    public get primaryRow(): RowNode<TData> {
        let node = (this.footer && this.sibling) || this;
        if (node.rowPinned) {
            const pinnedSibling = node.pinnedSibling;
            if (pinnedSibling) {
                node = pinnedSibling;
                if (node.footer) {
                    node = node.sibling ?? node;
                }
            }
        }
        return node as RowNode<TData>;
    }

