import { _camelCaseToHumanText } from 'ag-stack';

import type { NamedBean } from '../context/bean';
import { BeanStub } from '../context/beanStub';
import type { ColDef, ColGroupDef } from '../entities/colDef';
import type { AutoGenerateColumnDefsOptions } from '../entities/gridOptions';
import { _isPlainObject } from '../utils/mergeDeep';

export class AutoGenerateColumnsService extends BeanStub implements NamedBean {
    beanName = 'autoGenColsSvc' as const;

    public postConstruct(): void {
        this.generateColumns(true);
        this.addManagedPropertyListener('rowData', () => this.generateColumns(false));
        this.addManagedPropertyListener('autoGenerateColumnDefs', () => this.generateColumns(false));
    }

    private generateColumns(isInitial: boolean): void {
        const gos = this.gos;
        const optionValue = gos.get('autoGenerateColumnDefs');
        if (!optionValue) {
            return;
        }

        const rowData = gos.get('rowData') ?? [];

        const config: AutoGenerateColumnDefsOptions = typeof optionValue === 'object' ? optionValue : {};
        let firstRow: Record<string, unknown> | undefined;
        for (let i = 0, len = rowData.length; i < len; ++i) {
            if (_isPlainObject(rowData[i])) {
                firstRow = rowData[i] as Record<string, unknown>;
                break;
            }
        }
        if (!firstRow) {
            if (!isInitial) {
                gos.updateGridOptions({ options: { columnDefs: [] }, source: 'optionsUpdated' });
            }
            return;
        }
        let columnDefs = _buildColumnDefs(firstRow, undefined, config);

        const processCallback = gos.getCallback('processAutoGeneratedColumnDefs');
        if (processCallback) {
            const result = processCallback({ columnDefs, rowData });
            // If the return type is an array we use that, otherwise assume they have mutated the existing columnDefs
            if (Array.isArray(result)) {
                columnDefs = result;
            }
        }

        gos.updateGridOptions({ options: { columnDefs }, source: 'optionsUpdated' });
    }
}

function _buildColumnDefs(
    obj: Record<string, unknown>,
    prefix: string | undefined,
    config: AutoGenerateColumnDefsOptions,
    visited: Set<object> = new Set()
): (ColDef | ColGroupDef)[] {
    const { objectValues = 'group', arrayValues = 'primitives', nullishValues = 'include' } = config;

    const keys = Object.keys(obj);
    const defs: (ColDef | ColGroupDef)[] = [];
    for (let i = 0, len = keys.length; i < len; ++i) {
        const key = keys[i];
        const value = obj[key];
        const field = prefix ? `${prefix}.${key}` : key;

        if (_isPlainObject(value)) {
            if (visited.has(value)) {
                continue;
            }
            visited.add(value);
            if (objectValues === 'group') {
                const children = _buildColumnDefs(value, field, config, visited);
                if (children.length > 0) {
                    defs.push({ headerName: _camelCaseToHumanText(key) ?? key, children });
                }
            } else if (objectValues === 'flatten') {
                const children = _buildColumnDefs(value, field, config, visited);
                defs.push(...children);
            }
            continue;
        } else if (Array.isArray(value)) {
            if (arrayValues === 'skip' || (arrayValues === 'primitives' && !_isPrimitiveArray(value))) {
                continue;
            }
            defs.push(_makeLeafColDef(field, key, prefix, true));
            continue;
        } else if (value == null) {
            if (nullishValues !== 'include') {
                continue;
            }
        } else if (typeof value === 'function') {
            continue;
        }
        defs.push(_makeLeafColDef(field, key, prefix));
    }
    return defs;
}

function _makeLeafColDef(field: string, key: string, prefix: string | undefined, isArray?: boolean): ColDef {
    const colDef: ColDef = { field };
    if (prefix) {
        colDef.headerName = _camelCaseToHumanText(key) ?? key;
    }
    if (isArray) {
        colDef.cellDataType = false;
    }
    return colDef;
}

function _isPrimitiveArray(arr: unknown[]): boolean {
    if (arr.length === 0) {
        return false;
    }
    const first = arr[0];
    return first != null && !Array.isArray(first) && !_isPlainObject(first) && typeof first !== 'function';
}
